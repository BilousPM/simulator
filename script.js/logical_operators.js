console.log(Boolean(5));

// Логічні оператори
//   ---- логічне "і"  (оператор &&)
// запинається на False (неправді)
// Повертає то на чому запнулось, або останій оперант

console.log(5 && false);

//   ---- логічне "чи"  (оператор ||)
// запинається на True (правді)
// Повертає то на чому запнулось, або останій оперант

console.log(0 || NaN || 8);



//Логічне 'НЕ' (оператор "!")

console.log(!false);

 